package pl.tomaszJava.logic;

import pl.tomaszJava.data.*;

public class PointController
{
    public void addX(Point p) { p.setX(p.getX()+1); }
    public void minusX(Point p1) { p1.setX(p1.getX()-1); }
    public void addY(Point p2) { p2.setY(p2.getX()+1); }
    public void minusY(Point p3) { p3.setY(p3.getY()-1); }



}

